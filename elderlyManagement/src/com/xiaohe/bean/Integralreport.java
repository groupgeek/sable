package com.xiaohe.bean;

public class Integralreport {
    private Integer id;

    private Integer maxintegral;

    private Integer minintegral;

    private Integer countperson;

    private Integer branchid;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getMaxintegral() {
        return maxintegral;
    }

    public void setMaxintegral(Integer maxintegral) {
        this.maxintegral = maxintegral;
    }

    public Integer getMinintegral() {
        return minintegral;
    }

    public void setMinintegral(Integer minintegral) {
        this.minintegral = minintegral;
    }

    public Integer getCountperson() {
        return countperson;
    }

    public void setCountperson(Integer countperson) {
        this.countperson = countperson;
    }

    public Integer getBranchid() {
        return branchid;
    }

    public void setBranchid(Integer branchid) {
        this.branchid = branchid;
    }
}